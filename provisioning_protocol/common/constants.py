# Message Type Codes
MSG_BEACON       = 0x01
MSG_CHALLENGE    = 0x02
MSG_RESPONSE     = 0x03
MSG_CONFIRMATION = 0x04
MSG_ACK          = 0x05

# Field sizes in bytes
DEVICE_ID_SIZE   = 4
NONCE_SIZE       = 16
HASH_SIZE        = 32
TIMESTAMP_SIZE   = 8
ADDRESS_SIZE     = 2

# Status codes
STATUS_SUCCESS   = 0x00
STATUS_FAILURE   = 0x01